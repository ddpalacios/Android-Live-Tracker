package com.example.cta_map.Activities;

public class TrackingStation {
    public String trainLine, trainDir, station_name;

    public String getTrainLine() {
        return trainLine;
    }

    public void setTrainLine(String line) {
        trainLine = line;
    }

    public String getTrainDir() {
        return trainDir;
    }

    public void setTrainDir(String dir) {
        trainDir = dir;
    }

    public String getStationName() {
        return station_name;
    }

    public void setStationName(String name) {
        station_name = name;
    }


}
